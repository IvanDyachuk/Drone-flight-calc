# --- –î–ï–¢–ê–õ–¨ 1: –ë–ê–¢–ê–†–ï–Ø ---
class LipoBattery:
    def __init__(self, cells, capacity):
        self.cells = cells          # –°–∫–æ–ª—å–∫–æ –±–∞–Ω–æ–∫ (S)
        self.capacity = capacity    # –ï–º–∫–æ—Å—Ç—å (mAh)
        self.voltage = cells * 3.7  # –ù–æ–º–∏–Ω–∞–ª—å–Ω–æ–µ –Ω–∞–ø—Ä—è–∂–µ–Ω–∏–µ

    # –ú–µ—Ç–æ–¥, –∫–æ—Ç–æ—Ä—ã–π –∫—Ä–∞—Å–∏–≤–æ –æ–ø–∏—Å—ã–≤–∞–µ—Ç –±–∞—Ç–∞—Ä–µ—é
    def describe(self):
        return f"{self.cells}S LiPo ({self.voltage:.1f}V, {self.capacity}mAh)"

# --- –î–ï–¢–ê–õ–¨ 2: –î–†–û–ù ---
class FPVDrone:
    # –í–ù–ò–ú–ê–ù–ò–ï: –°—é–¥–∞ –º—ã –ø–µ—Ä–µ–¥–∞–µ–º battery_object (–≤—Å—é –±–∞—Ç–∞—Ä–µ—é —Ü–µ–ª–∏–∫–æ–º!)
    def __init__(self, name, battery_object):
        self.name = name
        self.battery = battery_object  # <-- –î—Ä–æ–Ω –∑–∞–ø–æ–º–Ω–∏–ª —Å–≤–æ—é –±–∞—Ç–∞—Ä–µ—é –≤–Ω—É—Ç—Ä–∏ —Å–µ–±—è

    def status(self):
        print(f"üöÅ –î—Ä–æ–Ω: {self.name}")
        # –î—Ä–æ–Ω –æ–±—Ä–∞—â–∞–µ—Ç—Å—è –∫ –°–í–û–ï–ô –±–∞—Ç–∞—Ä–µ–µ –∏ –ø—Ä–æ—Å–∏—Ç –µ—ë –æ–ø–∏—Å–∞—Ç—å —Å–µ–±—è
        print(f"üîã –ü–∏—Ç–∞–Ω–∏–µ: {self.battery.describe()}")

    def calculate_time(self, amps):
        # –î—Ä–æ–Ω –ª–µ–∑–µ—Ç –≤ —Å–≤–æ–π—Å—Ç–≤–∞ —Å–≤–æ–µ–π –±–∞—Ç–∞—Ä–µ–∏, —á—Ç–æ–±—ã –ø–æ—Å—á–∏—Ç–∞—Ç—å –≤—Ä–µ–º—è
        # self.battery.capacity ‚Äî —ç—Ç–æ –¥–æ—Å—Ç—É–ø –∫ –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π –í–ù–£–¢–†–ò –±–∞—Ç–∞—Ä–µ–∏
        hours = (self.battery.capacity / 1000) / amps
        return hours * 60

# --- –°–ë–û–†–ö–ê (–ì–õ–ê–í–ù–´–ô –ë–õ–û–ö) ---

# 1. –°–Ω–∞—á–∞–ª–∞ –±–µ—Ä–µ–º –±–∞—Ç–∞—Ä–µ—é —Å–æ —Å–∫–ª–∞–¥–∞ (–°–æ–∑–¥–∞–µ–º –æ–±—ä–µ–∫—Ç –±–∞—Ç–∞—Ä–µ–∏)
my_battery = LipoBattery(6, 1550)  # 6S 1550mAh

# 2. –¢–µ–ø–µ—Ä—å —Å–æ–±–∏—Ä–∞–µ–º –¥—Ä–æ–Ω –∏ –í–°–¢–ê–í–õ–Ø–ï–ú –≤ –Ω–µ–≥–æ —ç—Ç—É –±–∞—Ç–∞—Ä–µ—é
# –ü–µ—Ä–µ–¥–∞–µ–º 'my_battery' –∫–∞–∫ –∞—Ä–≥—É–º–µ–Ω—Ç!
kamikaze = FPVDrone("Chimera-7", my_battery)

# 3. –ü—Ä–æ–≤–µ—Ä–∫–∞ —Å–∏—Å—Ç–µ–º
print("--- –°–ò–°–¢–ï–ú–ù–´–ô –¢–ï–°–¢ ---")
kamikaze.status()

# 4. –†–∞—Å—á–µ—Ç
flight_time = kamikaze.calculate_time(25) # –¢–æ–∫ 25 –ê–º–ø–µ—Ä
print(f"‚è±Ô∏è –†–∞—Å—á–µ—Ç–Ω–æ–µ –≤—Ä–µ–º—è: {flight_time:.1f} –º–∏–Ω")